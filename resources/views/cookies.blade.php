                    <h5>¿QUÉ SON LAS COOKIES? </h5>
                    Una cookie es un fichero que se descarga en el disco duro de su ordenador al acceder a determinadas páginas web o aplicaciones. Las cookies permiten, entre otras cosas, recopilar información estadística, facilitar ciertas funcionalidades técnicas, almacenar y recuperar información sobre los hábitos de navegación o preferencias de un usuario o de su equipo y, dependiendo de la información que contengan y de la forma en que utilice su equipo, pueden utilizarse para reconocer al usuario. Tenga en cuenta que las cookies no pueden dañar su equipo y que, a cambio, el que estén activadas nos ayudan a identificar y resolver los posibles errores.
                    <br/><br/>

                    <h5>Las cookies utilizadas en http://www.vivetulinea.es</h5>

                    • Cookies estrictamente necesarias como por ejemplo, aquellas que sirven para una correcta navegación y aseguran que el contenido de la página web se carga eficazmente.
                    <br/><br/>
                    • Cookies analíticas, son cookies utilizadas para el análisis, investigación o estadísticas con el fin de mejorar la experiencia del sitio web. Mediante el uso de estas cookies se puede mostrar información más relevante a los visitantes.
                    <br/><br/>
                    • Cookies de terceros como por ejemplo, las usadas por las redes sociales, o por complementos externos de contenido como google maps.
                    <br/><br/>


                    <h3>Consentimiento</h3>

                    El usuario por la mera continuación en la navegación por el presente sitio web acepta la presente política de cookiesy consiente el uso de las mismas. El almacenamiento y tratamiento de la información así obtenida obedece a una finalidad técnica, al fin de efectuar la transmisión de una comunicación por una red de comunicaciones electrónicas, así como poder llevar a cabo la prestación de un servicio de la sociedad de la información expresamente solicitado por el usuario registrado.
                    <br/><br/>
                    En cualquier caso, el usuario puede acceder al menú de preferencias de su navegador y configurarlo para que no se instalen cookies, borrar las ya existentes y/o eliminarlas antes de iniciar la navegación por otras páginas del Sitio Web. Asimismo, puede utilizar herramientas de bloqueo de cookies.
                    <br/><br/>


                    <h5>DESHABILITACIÓN DE COOKIES</h5>

                    Para conocer la forma en la que deshabilitar las cookies de su navegador, le indicamos los pasos a seguir en los principales navegadores:
